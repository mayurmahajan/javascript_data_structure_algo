function performtasks(person)
{
    console.log(person + " does")
    console.log("wake up");
    console.log("eat");
    console.log("sleep");
}

function performtask(person, hobby)
{
    console.log(person + hobby + " does")
    console.log("wake up");
    console.log("eat");
}

performtasks("mayur");
performtask("mayur", "travel");
